function obtenirTemaNavBarHtml(tema) {
    return `<a class='dropdown-item' href='./tema.php?id=${tema.id}'>${tema.id}. ${tema.name}</a>`;
}

function obtenirNomArchiu(link) {
    let parts = (/^(([A-Z]:)?[\.]?[\\{1,2} \/]?.*[\\{1,2}\/])*(.+)\.(.+)/gm).exec(link);
    return parts[3] + "." + parts[4];
}

